"""
Experiment Module
Runs comparisons between Fixed, Actuated, and RL agents and saves metrics.
"""

import pandas as pd
import numpy as np
from pathlib import Path
import sys
import time

sys.path.insert(0, str(Path(__file__).resolve().parent.parent.parent))
import config


class AgentExperiment:
    """Runs simulation experiments to compare agent performance."""
    
    def __init__(self):
        self.results = []
        
    def run_baseline_sim(self, name, use_actuated=False):
        """Run a simulation using SUMO's native fixed or actuated signals."""
        from src.sumo_simulation import SUMOSimulation
        import subprocess
        
        # Modify the network to be fixed or actuated
        # For simplicity in this script, we'll just use the default network
        # which we generated as 'actuated' in sumo_network.py.
        # A true rigorous test would regenerate the .net.xml here.
        
        print(f"Running baseline: {name}...")
        start_time = time.time()
        
        sim = SUMOSimulation(use_gui=False)
        df = sim.run()
        
        elapsed = time.time() - start_time
        
        if not df.empty:
            avg_speed = df["avg_speed_kmh"].mean()
            total_co2 = df["total_co2_g"].sum()
            total_wait = df["total_waiting_time_s"].sum()
            
            self.results.append({
                "Agent": name,
                "Avg Speed (km/h)": avg_speed,
                "Total CO2 (kg)": total_co2 / 1000,
                "Total Wait Time (s)": total_wait,
                "Execution Time (s)": elapsed
            })
            print(f"  Complete! Speed: {avg_speed:.1f}, CO2: {total_co2/1000:.1f}kg")
            
            # Save step data
            df["agent"] = name
            df.to_csv(config.RESULTS_DIR / f"sim_steps_{name.lower().replace(' ', '_')}.csv", index=False)
        
    def run_rl_eval(self, model_path=None):
        """Evaluate a trained RL model."""
        from src.agents.rl_agent import RLAgentTrainer
        
        print(f"Running RL Agent Evaluation...")
        start_time = time.time()
        
        trainer = RLAgentTrainer(use_gui=False)
        env = trainer.create_env()
        
        try:
            model = trainer.load(model_path)
            
            obs = env.reset()
            dones = [False]
            
            # Since sumo-rl handles the loop internally in the env for multi-agent,
            # this is a simplified evaluation loop for standard SB3 envs.
            step = 0
            while not dones[0] and step < config.SUMO_SIM_STEPS:
                action, _states = model.predict(obs, deterministic=True)
                obs, rewards, dones, info = env.step(action)
                step += 1
                
            elapsed = time.time() - start_time
            print(f"  RL Evaluation Complete! ({elapsed:.1f}s)")
            
            # Pull metrics from the CSV generated by sumo-rl
            rl_csv = config.RESULTS_DIR / "rl_logs.csv"
            if rl_csv.exists():
                df = pd.read_csv(rl_csv)
                self.results.append({
                    "Agent": "RL Agent (PPO)",
                    "Avg Speed (km/h)": df["system_mean_speed"].mean() * 3.6 if "system_mean_speed" in df.columns else 0,
                    "Total CO2 (kg)": df["system_total_co2_emission"].sum() / 1000 if "system_total_co2_emission" in df.columns else 0,
                    "Total Wait Time (s)": df["system_total_stopped"].sum() if "system_total_stopped" in df.columns else 0,
                    "Execution Time (s)": elapsed
                })
                
        except Exception as e:
            print(f"  RL Evaluation failed (model likely not trained yet): {e}")

    def run_all(self):
        """Run all comparisons."""
        print("\n" + "=" * 60)
        print("Running Agent Comparisons")
        print("=" * 60)
        
        self.run_baseline_sim("Fixed Timing", use_actuated=False)
        self.run_baseline_sim("Actuated", use_actuated=True)
        self.run_rl_eval()
        
        results_df = pd.DataFrame(self.results)
        
        if not results_df.empty:
            print("\n--- Final Comparison ---")
            print(results_df.to_string(index=False))
            results_df.to_csv(config.RESULTS_DIR / "agent_comparison.csv", index=False)
            
        return results_df


if __name__ == "__main__":
    exp = AgentExperiment()
    exp.run_all()
